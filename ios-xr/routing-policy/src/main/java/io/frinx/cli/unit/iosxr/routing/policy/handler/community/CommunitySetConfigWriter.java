package io.frinx.cli.unit.iosxr.routing.policy.handler.community;

import io.fd.honeycomb.translate.write.WriteContext;
import io.fd.honeycomb.translate.write.WriteFailedException;
import io.frinx.cli.io.Cli;
import io.frinx.cli.unit.utils.CliWriter;
import org.jetbrains.annotations.NotNull;
import org.opendaylight.yang.gen.v1.http.frinx.openconfig.net.yang.bgp.policy.rev170730.community.set.top.community.sets.community.set.Config;
import org.opendaylight.yangtools.yang.binding.InstanceIdentifier;

public class CommunitySetConfigWriter implements CliWriter<Config> {

    private static final String TEMPLATE = "{.if ($delete) }no {/if}community-set {$config.community_set_name}\n"
            + "{.if ($delete) }{.else}end-set{/if}";

    private final Cli cli;

    public CommunitySetConfigWriter(Cli cli) {
        this.cli = cli;
    }

    @Override
    public void writeCurrentAttributes(@NotNull InstanceIdentifier<Config> id,
                                       @NotNull Config config,
                                       @NotNull WriteContext writeContext) throws WriteFailedException {
        blockingWriteAndRead(cli, id, config,
                fT(TEMPLATE, "config", config));

        // FIXME add members
    }

    @Override
    public void updateCurrentAttributes(@NotNull InstanceIdentifier<Config> id,
                                        @NotNull Config dataBefore,
                                        @NotNull Config dataAfter,
                                        @NotNull WriteContext writeContext) throws WriteFailedException {
        // FIXME this should just call write
    }

    @Override
    public void deleteCurrentAttributes(@NotNull InstanceIdentifier<Config> id,
                                        @NotNull Config config,
                                        @NotNull WriteContext writeContext) throws WriteFailedException {
        blockingWriteAndRead(cli, id, config,
                fT(TEMPLATE,
                        "config", config,
                        "delete", true));
    }
}