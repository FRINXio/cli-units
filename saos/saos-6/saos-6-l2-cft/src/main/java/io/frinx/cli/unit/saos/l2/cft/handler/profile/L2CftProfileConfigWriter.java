package io.frinx.cli.unit.saos.l2.cft.handler.profile;

import io.fd.honeycomb.translate.write.WriteContext;
import io.fd.honeycomb.translate.write.WriteFailedException;
import io.frinx.cli.io.Cli;
import io.frinx.cli.unit.utils.CliWriter;
import org.jetbrains.annotations.NotNull;
import org.opendaylight.yang.gen.v1.http.frinx.openconfig.net.yang.l2.cft.rev200416.l2.cft.top.l2.cft.profiles.profile.Config;
import org.opendaylight.yangtools.yang.binding.InstanceIdentifier;

public class L2CftProfileConfigWriter implements CliWriter<Config> {

    private static final String WRITE_PROFILE = "l2-cft create profile {$data.name}";
    private static final String DELETE_PROFILE = "l2-cft delete profile {$data.name}";

    private Cli cli;

    public L2CftProfileConfigWriter(Cli cli) {
        this.cli = cli;
    }

    @Override
    public void writeCurrentAttributes(@NotNull InstanceIdentifier<Config> instanceIdentifier,
                                       @NotNull Config config,
                                       @NotNull WriteContext writeContext) throws WriteFailedException {
        blockingWriteAndRead(fT(WRITE_PROFILE, "data", config), cli, instanceIdentifier, config);
    }

    @Override
    public void updateCurrentAttributes(@NotNull InstanceIdentifier<Config> id,
                                        @NotNull Config dataBefore,
                                        @NotNull Config dataAfter,
                                        @NotNull WriteContext writeContext) throws WriteFailedException {
        throw new WriteFailedException.UpdateFailedException(id, dataBefore, dataAfter,
                new IllegalArgumentException("l2-cft profile cannot be updated"));
    }

    @Override
    public void deleteCurrentAttributes(@NotNull InstanceIdentifier<Config> instanceIdentifier,
                                        @NotNull Config config,
                                        @NotNull WriteContext writeContext) throws WriteFailedException {
        blockingDeleteAndRead(fT(DELETE_PROFILE, "data", config), cli, instanceIdentifier);
    }
}